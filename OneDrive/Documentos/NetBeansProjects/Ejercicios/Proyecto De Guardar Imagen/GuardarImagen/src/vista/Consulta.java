    private void jButton_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConsultarActionPerformed
        
        if(txt_ID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Imgrese un ID para ejecutar consulta.");
        }else{
            
            String id_ing = "";
            id_ing = txt_ID.getText().trim().toString();
            //parseo
            int ID;
            ID = Integer.parseInt(id_ing);
            
            try {
                
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("select * from fotos where id_foto = '" + ID + "'");
                ResultSet rs = pst.executeQuery();
                
                if(rs.next()){
                    //Datos consultados
                    txt_nombre.setText(rs.getString("nombre"));
                    //leer Binario
                    Blob blob = rs.getBlob(3);
                    //pasar el binario a imagen
                    byte[] data = blob.getBytes(1, (int) blob.length());
                    //lee la imagen
                    BufferedImage img = null;
                    try {
                        img = ImageIO.read(new ByteArrayInputStream(data));
                    } catch (IOException e) {
                        Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, e);
                    }
                    
                    ImageIcon icono = new ImageIcon(img);
                    Icon imagen = new ImageIcon(icono.getImage().getScaledInstance(jLabel_foto.getWidth(), jLabel_foto.getHeight(), Image.SCALE_DEFAULT));
                    jLabel_foto.setIcon(imagen);
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Â¡Error al cargar!");
                System.out.println("Error al cargar foto: " + e);
            }
            
        }
        
        
        
        
    }//GEN-LAST:event_jButton_ConsultarActionPerformed

